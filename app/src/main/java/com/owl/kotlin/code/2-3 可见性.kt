 * internal   模块内可见- 一个 jar 包/一个 aar, 修饰的类和成员在 Java 中可直接访问(internal 在 java 中会被识别为 public)，可通过 @JvmName 注解设置方法/属性别名
 * private    本类中可见
 * protected  本类和子类可见
 *
 * 属性可见性
 *   property 为 public , 私有属性的 set，外部只能读取
 *   property 为 private, 不能私有属性的 set
 * */





